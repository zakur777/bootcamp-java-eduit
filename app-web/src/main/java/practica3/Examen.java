package practica3;

public class Examen {
	
	private int numeroExamen;
	private int nota;
	
	public Examen() {
		
	}
	
	public Examen(int numeroExamen, int nota) {
		super();
		this.numeroExamen = numeroExamen;
		this.nota = nota;
	}

	public int getNumeroExamen() {
		return numeroExamen;
	}

	public void setNumeroExamen(int numeroExamen) {
		this.numeroExamen = numeroExamen;
	}

	public int getNota() {
		return nota;
	}

	public void setNota(int nota) {
		this.nota = nota;
	}
	
	
	
	

}
